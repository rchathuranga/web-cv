$(window).on('load',function () {
    let pathTOJson = "js/particlejs/particles_json.json";
    if (window.matchMedia) {
        if(window.matchMedia('(prefers-color-scheme: dark)').matches){
            pathTOJson = "js/particlejs/particles_json_dark.json";
        } else {}
    } else {}
    particlesJS.load('particles-js', pathTOJson, function() {});
});